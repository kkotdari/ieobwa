DROP TABLE BOARD;
DROP TABLE WORD;
DROP TABLE REPLY;

/*
 * 게시판 테이블
 */
CREATE TABLE BOARD (
	B_NO INT PRIMARY KEY AUTO_INCREMENT COMMENT '글 번호',
	B_DATE DATETIME DEFAULT (CURRENT_TIME) COMMENT '글 작성일',
	B_WRITER VARCHAR(50) NOT NULL COMMENT '작성자',
	B_PASSWORD VARCHAR(50) NOT NULL COMMENT '암호',
	B_TITLE VARCHAR(500) NOT NULL COMMENT '글 제목',
	B_CONTENT VARCHAR(8000) NOT NULL COMMENT '글 내용'
) COMMENT '게시판';

 /*
 * 전체 단어 테이블
 */
CREATE TABLE WORD (
	W_NO INT PRIMARY KEY AUTO_INCREMENT COMMENT '단어 등록 번호',
	B_NO INT NOT NULL COMMENT '단어가 쓰인 게시글 번호',
	W_WORD VARCHAR(60) NOT NULL COMMENT '단어',
	W_FOUND INT NOT NULL COMMENT '해당 게시글에서 해당 단어가 쓰인 횟수',
	W_RATIO INT NOT NULL COMMENT '해당 게시글에서 전체 단어 중 해당 단어의 비율'
) COMMENT '단어';

/*
 * 댓글 테이블
 */
CREATE TABLE REPLY (
	R_NO INT PRIMARY KEY AUTO_INCREMENT COMMENT '댓글 번호',
	B_NO INT NOT NULL COMMENT '글 번호',
	P_NO INT NOT NULL COMMENT '대댓글의 경우 부모 댓글 번호',
	R_STEP INT NOT NULL COMMENT '댓글 단계',
	R_DATE DATETIME DEFAULT (CURRENT_TIME) COMMENT '댓글 작성일',
	R_WRITER VARCHAR(50) NOT NULL COMMENT '작성자',
	R_PASSWORD VARCHAR(50) NOT NULL COMMENT '암호',
	R_CONTENT VARCHAR(200) NOT NULL COMMENT '댓글 내용'
) COMMENT '댓글';